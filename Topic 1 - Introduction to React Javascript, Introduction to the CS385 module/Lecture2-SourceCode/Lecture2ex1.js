import React from "react";

/**
 * The App component in React.
 * App is just a common name for a component that
 * wraps the rest of your app.
 * There's nothing particularly special about it.
 * Here is one of the simplest React applications we could have
 */

function App() {
  return (
    <>
      <h1>Hello and welcome to CS385</h1>
      <p>This is Mobile App Development</p>
    </>
  );
}

/** Essentially your app is contained within the return
 *  statement in the App() function. This is the same for the simplest
 * applications and really complex production release apps
 */
export default App;

